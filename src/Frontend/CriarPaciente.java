    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nome= jTextField1.getText();
        String localidade = jTextField2.getText();
        //int codigo = (int) jSpinner1.getValue();
        Calendar entrada = jDateChooser1.getCalendar();
        
        String codE = jComboBox1.getSelectedItem().toString();
        Enfermaria e1 = null;
        for(Enfermaria e : hosp.getListaEnfermaria()){
            if(e.getCodEnf().equals(codE)){
                e1= e;
                break;
            }
                       
        }
        String estado = (String) jComboBox2.getSelectedItem();
        System.out.println(e1.getCodEnf());
        try{
            Paciente p = new Paciente(nome, localidade, "p"+sist.codigoUnico, e1, entrada, estado);
            
        
        System.out.println(p.getLocalidade());
        hosp.adcionarPaciente(p);
        mPac.tabela.fireTableDataChanged();
       
        e1.setPaciente(p);
        
            for(HashMap.Entry<Integer, Paciente> entry : e1.getCamaPaciente().entrySet()){
           
            if(entry.getValue()== p){
                int i= entry.getKey();
                p.setCama(i);
            }
        }
        
       
        
       
        
        sist.codigoUnico++;
        
        sist.gravarSistema();
        System.out.println(p.getCama());
        this.dispose();
        
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,
    "Selecione outra enfermaria para colocar o Paciente",
    "Enfermaria Cheia",
    JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

